function bulbOn(){
    let bulbobj = document.querySelector(".bulboff");
    bulbobj.setAttribute("src" , "https://www.w3schools.com/js/pic_bulbon.gif");
}

function bulbOff(){
    let bulbobj = document.querySelector(".bulboff");
    bulbobj.setAttribute("src" , " https://www.w3schools.com/js/pic_bulboff.gif");
}